    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'customer_name' => 'required',
            'customer_email' => 'required|email',
        ]);

        $customer = Customer::where('customer_email', '=', $request->get('customer_email'))->first();

        if (!$customer) {
            $customer = new Customer;

            $customer->customer_name = $request->get('customer_name');
            $customer->customer_email = $request->get('customer_email');
            $customer->customer_mobile = $request->get('customer_mobile');

            $customer->save();
        }

        session()->put('customer_id', $customer->id);
        session()->put('product_id', $request->get('product_id'));

        return redirect("/orders/create");
    }

